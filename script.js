const setLanguage = (lang) => {
  const dictionary = translations[lang];
  if (!dictionary) {
    return;
  }

  document.documentElement.lang = lang;
  localStorage.setItem("siteLang", lang);

  document.querySelectorAll("[data-i18n]").forEach((el) => {
    const key = el.getAttribute("data-i18n");
    const value = dictionary[key];
    if (value !== undefined) {
      el.textContent = value;
    }
  });

  document.querySelectorAll("[data-i18n-html]").forEach((el) => {
    const key = el.getAttribute("data-i18n-html");
    const value = dictionary[key];
    if (value !== undefined) {
      el.innerHTML = value;
    }
  });

  document.querySelectorAll("[data-i18n-placeholder]").forEach((el) => {
    const key = el.getAttribute("data-i18n-placeholder");
    const value = dictionary[key];
    if (value !== undefined) {
      el.setAttribute("placeholder", value);
    }
  });

  document.querySelectorAll("[data-i18n-alt]").forEach((el) => {
    const key = el.getAttribute("data-i18n-alt");
    const value = dictionary[key];
    if (value !== undefined) {
      el.setAttribute("alt", value);
    }
  });

  document.querySelectorAll("[data-i18n-aria]").forEach((el) => {
    const key = el.getAttribute("data-i18n-aria");
    const value = dictionary[key];
    if (value !== undefined) {
      el.setAttribute("aria-label", value);
    }
  });

  document.querySelectorAll("[data-lang]").forEach((btn) => {
    btn.classList.toggle("is-active", btn.dataset.lang === lang);
  });
};

const initLanguage = () => {
  const savedLang = localStorage.getItem("siteLang");
  const initial = savedLang || "uk";
  setLanguage(initial);

  document.querySelectorAll("[data-lang]").forEach((btn) => {
    btn.addEventListener("click", () => {
      setLanguage(btn.dataset.lang);
    });
  });
};

const initNavToggle = () => {
  const navToggle = document.querySelector(".nav-toggle");
  const siteNav = document.getElementById("site-nav");
  const navBackdrop = document.querySelector(".nav-backdrop");
  const navClose = siteNav ? siteNav.querySelector(".nav-close") : null;
  let lastFocused = null;
  if (!navToggle || !siteNav) {
    return;
  }

  const setNavState = (isOpen) => {
    const changed = isOpen !== siteNav.classList.contains("is-open");
    siteNav.classList.toggle("is-open", isOpen);
    navToggle.setAttribute("aria-expanded", String(isOpen));
    document.body.classList.toggle("nav-open", isOpen);
    if (navBackdrop) {
      navBackdrop.classList.toggle("is-open", isOpen);
    }
    const isOverlay = window.matchMedia("(max-width: 960px)").matches;
    if (isOverlay) {
      siteNav.setAttribute("aria-hidden", String(!isOpen));
      siteNav.toggleAttribute("inert", !isOpen);
    } else {
      siteNav.removeAttribute("aria-hidden");
      siteNav.removeAttribute("inert");
    }
    if (isOpen) {
      if (changed) {
        lastFocused = document.activeElement;
        const firstLink = siteNav.querySelector("a");
        if (firstLink) {
          firstLink.focus();
        }
      }
    } else if (changed && lastFocused && typeof lastFocused.focus === "function") {
      lastFocused.focus();
    }
  };

  navToggle.addEventListener("click", () => {
    const isOpen = !siteNav.classList.contains("is-open");
    setNavState(isOpen);
  });

  if (navClose) {
    navClose.addEventListener("click", () => setNavState(false));
  }

  if (navBackdrop) {
    navBackdrop.addEventListener("click", () => setNavState(false));
  }

  document.addEventListener("keydown", (event) => {
    if (event.key === "Escape" && siteNav.classList.contains("is-open")) {
      setNavState(false);
    }
  });

  siteNav.querySelectorAll("a").forEach((link) => {
    link.addEventListener("click", () => {
      if (siteNav.classList.contains("is-open")) {
        setNavState(false);
      }
    });
  });

  window.addEventListener("resize", () => {
    const isOverlay = window.matchMedia("(max-width: 960px)").matches;
    if (siteNav.classList.contains("is-open") && !isOverlay) {
      // crossed to desktop while the drawer was open — reset locked state
      setNavState(false);
    } else if (!siteNav.classList.contains("is-open")) {
      // keep inert / aria-hidden in sync with the current breakpoint
      setNavState(false);
    }
  });

  setNavState(false);
};

const initReveal = () => {
  const reveals = document.querySelectorAll("[data-reveal]");
  if (!("IntersectionObserver" in window)) {
    reveals.forEach((el) => el.classList.add("is-visible"));
    return;
  }

  const observer = new IntersectionObserver(
    (entries, obs) => {
      entries.forEach((entry) => {
        if (entry.isIntersecting) {
          entry.target.classList.add("is-visible");
          obs.unobserve(entry.target);
        }
      });
    },
    { threshold: 0.2 }
  );

  reveals.forEach((el) => observer.observe(el));
};

const setYear = () => {
  const year = new Date().getFullYear();
  document.querySelectorAll("[data-year]").forEach((el) => {
    el.textContent = year;
  });
};

const initHeaderScroll = () => {
  const header = document.querySelector(".site-header");
  if (!header) {
    return;
  }
  const onScroll = () => {
    header.classList.toggle("is-scrolled", window.scrollY > 12);
  };
  onScroll();
  window.addEventListener("scroll", onScroll, { passive: true });
};

